package zonaProp.transfer.forms;

public class Combo {

	private int pos;
	private String description;

	public Combo(int pos, String description) {
		super();
		this.pos = pos;
		this.description = description;
	}

	public int getPos() {
		return pos;
	}

	public String getDescription() {
		return description;
	}

	
}
